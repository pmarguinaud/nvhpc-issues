SUBROUTINE SUEHDF

IMPLICIT NONE

TYPE TT
  REAL*8 :: FRANDH
END TYPE

TYPE (TT) :: YDDYN

REAL(KIND=8) :: RDIVORE(100)

INTEGER(KIND=4) :: JLEV

REAL(KIND=8) :: BDISPE
INTEGER(KIND=4) :: KM
REAL(KIND=8) :: ZRANDH


BDISPE(KM)=REAL (KM, 8) + ZRANDH

ZRANDH = YDDYN%FRANDH

!$OMP PARALLEL DO PRIVATE(JLEV)
    DO JLEV=1,100
      RDIVORE(JLEV)=BDISPE(JLEV)
    ENDDO 
!$OMP END PARALLEL DO

END SUBROUTINE SUEHDF

