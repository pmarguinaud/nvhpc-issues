INTEGER :: ITIME

!$ACC ENTER DATA CREATE (ZSTACK)

DO ITIME = 1, 2

  WRITE (CLSTR, '(I6.6)') ITIME
  CALL NVTXSTARTRANGE (TRIM (CLSTR))
   
  CALL SUB0 (KLON, KGPBLKS)
  CALL SUB1 (KLON, KGPBLKS)
  CALL SUB2 (KLON, KGPBLKS)
  CALL SUB3 (KLON, KGPBLKS)
  CALL SUB4 (KLON, KGPBLKS)
  CALL SUB5 (KLON, KGPBLKS)
