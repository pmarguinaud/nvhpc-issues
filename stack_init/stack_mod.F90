MODULE STACK_MOD

IMPLICIT NONE

TYPE STACK
  INTEGER*8 :: L8
END TYPE

CONTAINS

TYPE (STACK) FUNCTION STACK_INIT_F (IBL) RESULT (SELF)

!$acc routine seq

TYPE (STACK) :: SELF
INTEGER, INTENT (IN) :: IBL

CALL STACK_INIT_S (SELF, IBL)

END FUNCTION 
 
SUBROUTINE STACK_INIT_S (SELF, IBL)

!$acc routine seq

TYPE (STACK) :: SELF
INTEGER, INTENT (IN) :: IBL

SELF%L8 = IBL 

END SUBROUTINE
 
END MODULE

