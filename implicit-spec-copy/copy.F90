PROGRAM MAIN

IMPLICIT NONE

INTEGER, PARAMETER :: M = 5, N = 10

REAL :: X (M, N)

PRINT *, LOC (X (M, 1))

CALL COPY1 (X (M, :))
CALL COPY2 (X (M:M, :))

CONTAINS

SUBROUTINE COPY1 (P)

REAL :: P (:)

PRINT *, LOC (P (1))

END SUBROUTINE

SUBROUTINE COPY2 (P)

REAL :: P (:, :)

PRINT *, LOC (P (1, 1))

END SUBROUTINE

END
